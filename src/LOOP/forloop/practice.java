package LOOP.forloop;

public class practice {
    public static void main(String[] args) {
        //print number from 0-10 inclusive
        int num=0;
        while(num<10){
            System.out.println(num);
            num++;
        }
        //print number from 0-10
        int num1=0;
        for( ;num1<=10; ){
            System.out.println(num1);
          num1++;
        }
        for( int a=0 ;a<=10   ; a++   ){
            System.out.println(a);

        }
    }

}
